    private void btnLoadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoadActionPerformed
        LoadRecord lr = new LoadRecord(patientDirectory);
        if (!isLoadAllDone) {
            lr.loadAll();
            isLoadAllDone = true;
            JOptionPane.showMessageDialog(this, "The available Patient Records are loaded !");
        } else {
            JOptionPane.showMessageDialog(this, "They are already loadded");
        }
    }//GEN-LAST:event_btnLoadActionPerformed
