    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        Person person = new Person();
        
        //validation ?
        
        person.setName(nameTextField.getText());
        person.setAge(Integer.parseInt(ageTextField.getText()));
        person.setHomeAddress(streetTextField.getText());
        person.setCity(cityTextField.getText());
        person.setContactDirectory(Long.parseLong(contactTextField.getText()));
        person.setCommunity(commGroup.getSelection().getActionCommand());
        
        Patient patient = patientDirectory.addNewPatient();
        
        patient.setPerson(person);
        
        //duplicacy ?
        
        clearFields();
        JOptionPane.showMessageDialog(this, "Patient Record is added !");
    }//GEN-LAST:event_btnAddActionPerformed

